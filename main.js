// Datatypes


//STATIC TYPE DATATYPE

// NUMBER DATATYPE
let numValue;// Declaration of variable
numValue=10;// initialize value
console.log(numValue);// 10 (output)
console.log(typeof(numValue));// number


// STRING DATATYPE
let strName;// Declaration of variable
strName = "Jueeli"; // initialize value
console.log(strName); // output= Jueeli
console.log(typeof(strName)); // string

//BOOLEAN DATATYPE
let boolValue; // Declaration of variable
boolValue= true; // initialize value 
console.log(boolValue); // true
console.log(typeof(boolValue)); // boolean


